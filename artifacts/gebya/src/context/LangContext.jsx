const DEFAULT_EN_TRANSLATIONS = { ...EN, ...EN_OVERRIDES };

const LangContext = createContext(null);

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('gebya_lang') || 'en');
  const [amTranslations, setAmTranslations] = useState(null);

  useEffect(() => {
    document.documentElement.lang = lang;
    document.body.dataset.lang = lang;
    localStorage.setItem('gebya_lang', lang);
  }, [lang]);

  useEffect(() => {
    if (lang !== 'am' || amTranslations) return;
    let cancelled = false;

    import('./langAm.js')
      .then(({ AM, AM_OVERRIDES }) => {
        if (!cancelled) {
          setAmTranslations({ ...AM, ...AM_OVERRIDES });
        }
      })
      .catch((error) => {
        if (import.meta.env.DEV) console.error('Failed to load Amharic translations:', error);
      });

    return () => {
      cancelled = true;
    };
  }, [amTranslations, lang]);

  const toggleLang = () => {
    setLang(prev => {
      const next = prev === 'en' ? 'am' : 'en';
      return next;
    });
  };

  const t = useMemo(() => {
    if (lang === 'am' && amTranslations) return amTranslations;
    return DEFAULT_EN_TRANSLATIONS;
  }, [amTranslations, lang]);

  return (
    <LangContext.Provider value={{ lang, toggleLang, t }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLang() {
  return useContext(LangContext);
}
